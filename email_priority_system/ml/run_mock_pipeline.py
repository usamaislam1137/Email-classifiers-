    with open(MODELS_DIR / "evaluation_report.json", "w") as fh:
        json.dump(eval_report, fh, indent=2, default=str)

    # Copy JSONs to evidence dir too
    with open(EVIDENCE_DIR / "training_results.json", "w") as fh:
        json.dump(training_results, fh, indent=2, default=str)
    with open(EVIDENCE_DIR / "evaluation_report.json", "w") as fh:
        json.dump(eval_report, fh, indent=2, default=str)

    log.info("JSON artefacts saved.")

    # 8. Generate evidence plots
