def plot_roc_curves(models_data: list[dict], X_test, y_test):
    """Plot one-vs-rest ROC curves for all models."""
    y_bin = label_binarize(y_test, classes=[0, 1, 2, 3])
    fig, axes = plt.subplots(1, len(models_data), figsize=(6 * len(models_data), 5))
    if len(models_data) == 1:
        axes = [axes]

    for ax, item in zip(axes, models_data):
        model     = item["model_obj"]
        name      = item["name"]
        X_input   = item.get("X_test_input", X_test)
        if hasattr(model, "predict_proba"):
            y_score = model.predict_proba(X_input)
        else:
            continue
        for cls_i, cls_name in enumerate(LABEL_NAMES):
            fpr, tpr, _ = roc_curve(y_bin[:, cls_i], y_score[:, cls_i])
            roc_auc = auc(fpr, tpr)
            ax.plot(fpr, tpr, lw=1.5, label=f"{cls_name} (AUC={roc_auc:.2f})")
        ax.plot([0,1],[0,1],"k--", lw=0.8)
        ax.set_xlim(0, 1); ax.set_ylim(0, 1.02)
        ax.set_xlabel("False Positive Rate", fontsize=10)
        ax.set_ylabel("True Positive Rate", fontsize=10)
        ax.set_title(f"ROC — {name.replace('_',' ').title()}", fontsize=11, fontweight="bold")
        ax.legend(fontsize=8, loc="lower right")

    plt.tight_layout()
    path = EVIDENCE_DIR / "roc_curves.png"
    fig.savefig(path, dpi=150)
    plt.close(fig)
    log.info("Saved ROC curves: %s", path)


