    bars1 = ax.bar(x - w/2, accs, w, label="Accuracy",  color="#4C72B0", alpha=0.85)
    bars2 = ax.bar(x + w/2, f1s,  w, label="Macro F1",  color="#DD8452", alpha=0.85)
    ax.set_ylim(0, 1.05)
    ax.set_xticks(x)
    ax.set_xticklabels(names, fontsize=11)
    ax.set_ylabel("Score", fontsize=12)
